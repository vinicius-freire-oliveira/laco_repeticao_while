# Inicializa a variável 'i' como 1
i = 1

# Inicia um loop while que continua enquanto 'i' for menor ou igual a 7
while(i <= 7):
    # Imprime o valor atual de 'i'
    print(i)
    # Incrementa 'i' em 1 para o próximo ciclo
    i = i + 1
    # Verifica se 'i' é igual a 5
    if(i == 5):
        # Se 'i' for igual a 5, interrompe o loop
        break
